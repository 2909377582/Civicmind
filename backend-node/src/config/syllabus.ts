export const getSyllabusByLevel = (level?: string): string => {
    if (!level) return HUNAN_SYLLABUS['省市卷']; // 默认
    if (level.includes('县') || level.includes('乡')) return HUNAN_SYLLABUS['县乡卷'];
    if (level.includes('执法')) return HUNAN_SYLLABUS['行政执法卷'];
    return HUNAN_SYLLABUS['省市卷'];
};
